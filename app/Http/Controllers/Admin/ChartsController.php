     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $parto = Labourvitalsign::orderBy('created_at')->pluck('FHR', 'created_at');

        $fhr = new Partograph;
        $fhr->labels($parto->keys());
        $fhr->dataset('Fetal Heart Rate', 'bar', $parto->values());

       
        return view('admin.charts.index', compact('fhr')); 
    }

